import matplotlib.pyplot as plt

# Create data
labels = ['A', 'B', 'C']
values = [10, 20, 30]

# Plot bar chart
plt.bar(labels, values)

# Set the title of the plot
plt.title('Bar Chart')

# Set the labels of the x-axis and y-axis
plt.xlabel('Labels')
plt.ylabel('Values')

# Show the plot
plt.show()
